void	ft_putnbr(int nb)
{
	unsigned int n;

	if (nb < 0)
	{
		wrtie(1, "-", 1);
		n = nb * -1;
	}
	else
	{
		n = nb;
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	write(1, n % 10 + '0', 1);
}
